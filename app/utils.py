def price_proc(price: int | str) -> str:
    """Форматирует числовую цену с пробелами в качестве разделителей тысяч.

    Пример::

        >>> price_proc(1000000)
        '1 000 000'

    Args:
        price: Целое число или строка, представляющая цену в рублях.

    Returns:
        Строка с отформатированной ценой, где каждые три цифры
        разделены пробелом (от конца).
    """
    price_str = str(price)
    result = ""
    length = len(price_str)

    for idx in range(length):
        # Добавляем символ в обратном порядке, вставляем пробел каждые 3 цифры
        char = price_str[length - idx - 1]
        result += char
        if (idx + 1) % 3 == 0 and idx + 1 < length:
            result += " "

    # Переворачиваем обратно и убираем случайный ведущий пробел
    return result[::-1].strip()


def set_session(db_path: str, brand_name: str) -> None:
    """Обновляет глобальное состояние текущей сессии конфигуратора.

    Вызывается при смене бренда/базы данных в главном меню,
    чтобы все окна видели актуальные значения.

    Args:
        db_path: Путь к файлу SQLite выбранного бренда.
        brand_name: Человекочитаемое название бренда.
    """
    global db, brand
    db = db_path
    brand = brand_name
